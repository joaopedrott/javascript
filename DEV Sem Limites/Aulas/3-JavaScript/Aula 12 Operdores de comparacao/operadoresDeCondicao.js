// Operadores Logicos de Condicao
/*
Igual (==)
Estritamente igual (===)
Diferente (!=)
Estritamente diferente (!==)
Maior que (>)
Menor que (<)
Maior ou igual que (>=)
Menor ou igual que (<=)

*/

//Igual (==)

console.log('Igual (==)')
console.log(2 == 2)
console.log(2 == '2') //Coercao de tipos
console.log(2==3)

//Estritamente igual (===)
console.log('Estritamente igual (===)')
console.log(2 === 2)
console.log(2 === '2')
console.log(2===3)

//Diferente (!=)
console.log('Diferente (!=)')
console.log(2 != 2)
console.log(2 != '2')
console.log(2!=3)

//Estritamente diferente (!==)
console.log('Estritamente diferente (!==)')
console.log(2 !== 2)
console.log(2 !== '2')
console.log(2!==3)

//Maior que (>)
console.log('Maior que (>)')
console.log(2>1)
console.log(1>2)

//Menor que (<)
console.log('Menor que (<)')
console.log(2<1)
console.log(1<2)

//Maior ou igual que (>=)
console.log('Maior ou igual que (>=)')
console.log(2>=1)
console.log(1>=2)

//Menor ou igual que (<=)
console.log('Menor ou igual que (<=)')
console.log(2<=1)
console.log(1<=2)